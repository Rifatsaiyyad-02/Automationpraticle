package ifelse;

public class forloop {

	public static void main(String[] args) {
		for(int i=0;i<5;i++) {
			System.out.println("hello good..");
		}
		for(int i=0;i<5;i++) {
			System.out.println(i);
		}
		for (int i=10;i>0;i--) {
			System.out.println(i);
		}for (char cl='a';cl<='z';cl++) {
			System.out.println(cl+"");
		}

	}

}
